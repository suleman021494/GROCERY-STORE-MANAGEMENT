    FROM products
    GROUP BY sup_id
) t
join supplier s 
on s.sup_id=t.sup_id
WHERE rnk = 1;

-- What is the average price of products from each supplier?
select sup_id,avg(price) avge
from products
group by sup_id;


-- Which suppliers contribute the most to total product sales (by revenue)?

SELECT t.sup_id,s.sup_name,
       revenue
FROM (
    SELECT p.sup_id,
           SUM(od.total_price) AS revenue,
           DENSE_RANK() OVER (
               ORDER BY SUM(od.total_price) DESC
           ) AS rnk
    FROM products p
    JOIN order_details od
        ON p.prod_id = od.prod_id
    GROUP BY p.sup_id
) t
join supplier s 
on s.sup_id=t.sup_id
WHERE rnk = 1;


