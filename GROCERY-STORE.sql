select emp_id,orders
from (
select emp_id,orders,dense_rank() over (
					order by orders desc
                    ) as rnk
from (select emp_id,count(*) orders
		from orders
		group by emp_id
        ) x
        ) y
        where rnk =1;


-- What is the total sales value processed by each employee?

SELECT o.emp_id,
       SUM(od.total_price) AS total_sales
FROM orders o
JOIN order_details od
    ON o.ord_id = od.ord_id
GROUP BY o.emp_id
ORDER BY o.emp_id;


-- What is the average order value handled per employee?

SELECT p.emp_id,
       AVG(o.total) AS avg_order_value
FROM (
    SELECT ord_id,
           SUM(total_price) AS total
    FROM order_details
    GROUP BY ord_id
) o
JOIN orders p
    ON p.ord_id = o.ord_id
GROUP BY p.emp_id
ORDER BY p.emp_id;


-- 6️. Order Details Deep Dive
-- What is the relationship between quantity ordered and total price?

SELECT quantity,
       AVG(total_price) AS avg_total_price,
       COUNT(*) AS orders
FROM order_details
GROUP BY quantity
ORDER BY quantity;


-- What is the average quantity ordered per product?

SELECT p.prod_name,
       AVG(od.quantity) AS avg_quantity
FROM products p
JOIN order_details od
    ON p.prod_id = od.prod_id
GROUP BY p.prod_id, p.prod_name
ORDER BY p.prod_id;


-- How does the unit price vary across products and orders?

SELECT p.prod_name,
       MIN(od.each_price) AS min_price,
       MAX(od.each_price) AS max_price,
       AVG(od.each_price) AS avg_price
FROM products p
JOIN order_details od
    ON p.prod_id = od.prod_id
GROUP BY p.prod_id, p.prod_name
ORDER BY p.prod_id;


